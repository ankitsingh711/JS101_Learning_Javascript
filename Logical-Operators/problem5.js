function checkDay(day){
  if (day == "Mon"){
    console.log("Monday");
  } else if(day == "Tue"){
    console.log("Tuesday");
  } else if(day == "Wed"){
    console.log("Wednesday");
  } else if(day == "Thu"){
    console.log("Thursday");
  } else if(day == "Fri"){
    console.log("Friday");
  } else if(day == "Sat"){
    console.log("Saturday");
  } else if(day == "Sun"){
    console.log("Sunday");
  }
}

checkDay("Mon");